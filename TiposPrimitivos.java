public class TiposPrimitivos {
	public static void main(String[] args){
		System.out.println("\nEsses são os TIPOS PRIMITIVOS em JAVA:\n");
		String string = "String";
		int integer = 1;
		byte bytte = 127;
		long longg = 833123910931237239L;
		float floatt = 3.14159236f;
		double dobw = 3848139230.323d;
		boolean yOrN = true;
		char c = 'C';

		System.out.printf("String => %s %n", string);
		System.out.printf("Integer => %d %n", integer);
		System.out.printf("Byte => %d %n", bytte);
		System.out.printf("Long => %d %n", longg);
		System.out.printf("Float => %f %n", floatt);
		System.out.printf("Double => %f %n", dobw);
		System.out.printf("Boolean => %b %n", yOrN);
		System.out.printf("Char => %c %n", c);

	}
}